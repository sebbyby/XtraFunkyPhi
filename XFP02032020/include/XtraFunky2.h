int expandArray(int* arPtr,int oldSize,int newSize)
{
    int lim = oldSize;

    if (newSize < lim)
    {
        lim = newSize;
    }

    int* temPtr = new int [oldSize];
    for (int i =0;i<oldSize;i++)
    {
        temPtr[i] = arPtr[i];
    }

    arPtr = new int [newSize];

    for (int i =0;i<lim;i++)
    {
        arPtr[i] = temPtr[i];
    }

    delete[] temPtr;
    temPtr = nullptr;

    return newSize;
}




